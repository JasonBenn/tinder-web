require 'date'
require 'time'

class LoginFailureError < StandardError
end

module Tinder
  extend self


  def sign_in
    @pyro = TinderPyro::Client.new
    fb_auth = YAML.load(File.open('fb_auth.yaml'))
    response = @pyro.sign_in(fb_auth['FACEBOOK_ID'], fb_auth['FACEBOOK_TOKEN'])
    raise LoginFailureError, response if response['code'] && response['error']
  end

  def fetch_updates
    @pyro.fetch_updates
  end

  def refresh_subscribed_users
    puts 'refreshing users...'
  end

  def sort_by_matched
    USERS
  end

  def sort_by_recently_online
    USERS.map do |user|
      user['age'] = Date.today.year - Date.parse(user['birth_date']).year
      user['last_active'] = (Time.parse(user['ping_time']) + SEVEN_HOURS).to_pretty
      user
    end
  end

end

SEVEN_HOURS = 7*60*60

module PrettyDate
  def to_pretty
    a = (Time.now-self).to_i
    puts a < 0

    case a
      when (a<0) then 'negative??'
      when 0 then 'just now'
      when 1 then 'a second ago'
      when 2..59 then a.to_s+' seconds ago' 
      when 60..119 then 'a minute ago' #120 = 2 minutes
      when 120..3540 then (a/60).to_i.to_s+' minutes ago'
      when 3541..7100 then 'an hour ago' # 3600 = 1 hour
      when 7101..82800 then ((a+99)/3600).to_i.to_s+' hours ago' 
      when 82801..172000 then 'yesterday' # 86400 = 1 day
      else ((a+800)/(60*60*24)).to_i.to_s+' days ago'
      # else a
      # else ((a+180000)/(60*60*24*7)).to_i.to_s+' weeks ago'
      # when 0 then 'just now'
